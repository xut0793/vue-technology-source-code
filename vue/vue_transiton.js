/**
 * Vue 动画同原生动画一样，基本分成：CSS 动画 和 JS 动画
 * 然后动画实现也分为两种：transition 过渡 和 animation 动画
 * 
 * 例子：transiton 过渡
 * <transition name="slide-fade">
      <p v-if="show">hello</p>
    </transition>

    .fade-enter-active, .fade-leave-active {
      transition: opacity .5s;
    }
    .fade-enter, .fade-leave-to {
      opacity: 0;
    }
 * 例子：animation 动画

    <transition name="bounce">
      <p v-if="show">Lorem ipsum dolor sit amet.</p>
    </transition>

    .bounce-enter-active {
      animation: bounce-in .5s;
    }
    .bounce-leave-active {
      animation: bounce-in .5s reverse;
    }
    @keyframes bounce-in {
      0% {
        transform: scale(0);
      }
      50% {
        transform: scale(1.5);
      }
      100% {
        transform: scale(1);
      }
    }

 * js 动画 Velocity 是一个 js 动画类库
    <transition
    v-on:before-enter="beforeEnter"
    v-on:enter="enter"
    v-on:leave="leave"
    v-bind:css="false"
  >
    <p v-if="show">
      Demo
    </p>
  </transition>

  methods: {
    beforeEnter: function (el) {
      el.style.opacity = 0
      el.style.transformOrigin = 'left'
    },
    enter: function (el, done) {
      Velocity(el, { opacity: 1, fontSize: '1.4em' }, { duration: 300 })
      Velocity(el, { fontSize: '1em' }, { complete: done })
    },
    leave: function (el, done) {
      Velocity(el, { translateX: '15px', rotateZ: '50deg' }, { duration: 600 })
      Velocity(el, { rotateZ: '100deg' }, { loop: 2 })
      Velocity(el, {
        rotateZ: '45deg',
        translateY: '30px',
        translateX: '30px',
        opacity: 0
      }, { complete: done })
    }
  }

  过滤模式
  <transition name="fade" mode="out-in">
    <!-- ... one real element ... -->
  </transition>
 */

 /**
  * 总结起来： Vue 不管是 CSS 动画，还是 JS 动画，基本过程依赖于 enter 和 leave 钩子函数
  * 一、CSS 动画依赖两个阶段六个类名配置：
  *   .v-enter .v-enter-active .v-enter-to
  *   .v-leave .v-leave-active .v-leave-to
  * 二、JS 动画依赖两个阶段八个事件：在 enter 和 leave 中必须使用 done 进行回调
  *   before-enter enter after-enter enter-cancelled
  *   before-leave leave after-leave leave-cancelled
  * 三、过渡模式：in-out out-in
  *   因为 <transition> 的默认行为是进入和离开同时发生，所以为了实现平滑动画，实现了过滤模式：
  *   in-out：新元素先进行过渡，完成之后当前元素过渡离开。
  *   out-in：当前元素先进行过渡，完成之后新元素过渡进入。
  * 四、初始渲染的过滤 appear
  * 五、type
  *   Vue 当过渡或动画完成时会移除对应的类名，在内部是通过设置相应的事件监听器实现，它可以是 transitionend 或 animationend，这取决于给元素应用的 CSS 规则。
  *   如果你使用其中任何一种，Vue 能自动识别类型并设置监听。
  *   但如果给同一个元素同时设置两种过渡动效，则需要使用 type 来设置值为 animation 或 transition ，用来明确声明你需要 Vue 监听动画结束的类型。
  */

